// ------------------------------------------------- >> layer 5 <<
const Layer5 = document.querySelector(".Layer5");
const BG_5 = document.querySelector("#BG_5");
// ------------------------------------------------- >> layer 5 <<
//
// =============================================================== >> Layers <<
//
// =============================================================== >> Widget <<
function Loading() {
  //
  // ------------------------------------------------ >> column <<
  const Loading = document.createElement("div");
  Loading.classList.add("Loading");
  // ------------------------------------------------ >> column <<
  //
  // -------------------------------------------------- >> Text <<
  const LoadingText = document.createElement("span");
  LoadingText.classList.add("LoadingText");
  //
  // ------------------------------------------ > src <
  LoadingText.textContent = "SAMAMI";
  // ------------------------------ > src <
  //
  // ------------------------------------------- > AC <
  Loading.appendChild(LoadingText);
  // ------------------------------------------- > AC <
  //
  // -------------------------------------------------- >> Text <<
  //
  // -------------------------------------------------- >> Icon <<
  const LoadingIcon = document.createElement("iconify-icon");
  LoadingIcon.classList.add("LoadingIcon");
  //
  // ----------------------------------------- > Value <
  LoadingIcon.setAttribute("icon", "eos-icons:three-dots-loading");
  // ----------------------------------------- > Value <
  //
  // ------------------------------------------- > AC <
  Loading.appendChild(LoadingIcon);
  // ------------------------------------------- > AC <
  //
  // -------------------------------------------------- >> Icon <<
  //
  // ------------------------------------------------ >> return <<
  return Loading;
  // ------------------------------------------------ >> return <<
  //
}
// =============================================================== >> Widget <<
//
// =================================================================== >> CB <<
export function LoadingcallBack(Status) {
  //
  // --------------------------------------------------- >> Loading <<
  //
  // -------------------------------------------- > CB <
  const Widget = Loading();
  // -------------------------------------------- > CB <
  //
  // --------------------------------------------------- >> Loading <<
  //
  // -------------------------------------------------------- >> SM <<
  //
  // ----------------------------------------------- > Show <
  if (Status == 1) {
    //
    // --------------------------------------- AC >>
    BG_5.appendChild(Widget);
    // --------------------------------------- AC <<
    //
    // --------------------------------------- SM >>
    setTimeout(() => {
      Layer5.classList.add("show");
    }, 1);
    // --------------------------------------- SM <<
    //
  }
  // ----------------------------------------------- > Show <
  //
  // --------------------------------------------- > Remove <
  else if (Status == 2) {
    //
    // --------------------------------------- SM >>
    Layer5.classList.remove("show");
    // --------------------------------------- SM <<
    //
    // --------------------------------------- AC >>
    setTimeout(() => {
      BG_5.removeChild(Widget);
    }, 500);
    // --------------------------------------- AC <<
    //
  }
  // --------------------------------------------- > Remove <
  //
  // -------------------------------------------------------- >> SM <<
  //
}
